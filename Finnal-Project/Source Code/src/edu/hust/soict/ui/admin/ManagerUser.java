    private void getListAccount() throws SQLException {
       CheckManagerUser checkManagerUser = new CheckManagerUser();
       ResultSet resultSet = checkManagerUser.getDataAccount();
                while (jTable1.getRowCount() > 0) {
                    ((DefaultTableModel) jTable1.getModel()).removeRow(0);
                }
                int col = resultSet.getMetaData().getColumnCount();
                while (resultSet.next()) {
                    Object[] rows = new Object[col];
                    for (int i = 1; i <= col; i++) {
                        rows[i - 1] = resultSet.getObject(i);
                    }
                    ((DefaultTableModel) jTable1.getModel()).insertRow(resultSet.getRow() - 1, rows);
                }
    }
