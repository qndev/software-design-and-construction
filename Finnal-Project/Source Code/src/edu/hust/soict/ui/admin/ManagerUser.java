    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        // make sure a row is selected
        if (row < 0) {
            JOptionPane.showMessageDialog(ManagerUser.this, "Bạn chưa chọn User cần thay đổi!", "Thông báo!",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        String tendangnhap = jTextField1.getText();
        String matkhau = jPasswordField1.getText();
        String vaitro = (String) jComboBox1.getSelectedItem();
        AddUser user = new AddUser(tendangnhap, matkhau, vaitro);
        boolean update = new AddUserDAO().updateUser2(user);
        if (update == true) {
            JOptionPane.showMessageDialog(ManagerUser.this, "Đã cập nhật dữ liệu thành công!", "Updated", JOptionPane.INFORMATION_MESSAGE);
            try {
                getListAccount();
            } catch (SQLException ex) {
                Logger.getLogger(ManagerUser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        jButton1.setEnabled(false);
        jButton3.setEnabled(true);
        jButton2.setEnabled(true);
        jButton4.setEnabled(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(ManagerUser.this, "Bạn chưa chọn User cần xóa!", "Thông báo!", JOptionPane.WARNING_MESSAGE);
        } else {
            int b = JOptionPane.showConfirmDialog(ManagerUser.this, "Bạn chắc chắn muốn xóa dữ liệu này?", "Thông báo!", JOptionPane.YES_NO_OPTION);
            if (b == JOptionPane.YES_OPTION) {
                try {
                    String tendangnhap = jTextField1.getText();
                    new AddUserDAO().deleteUser(tendangnhap);
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(ManagerUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            resetForm();
            jButton1.setEnabled(true);
            jButton3.setEnabled(true);
            jButton2.setEnabled(false);
            jButton4.setEnabled(true);
            jTextField1.setEnabled(true);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

